local bit32 = require("bit32")

local flag = {0x3d,0x3a,0x28,0x12,0x1e,0x01,0x10,0x36,0x0a,0x06,0x04,0x19,0x05,0x00,0x0a,0x08,0x1d,0x0c,0x36,0x1d,0x01,0x00,0x07,0x0e,0x1a,0x36,0x1e,0x01,0x0c,0x07,0x36,0x10,0x06,0x1c,0x36,0x0a,0x08,0x07,0x36,0x06,0x0b,0x0f,0x1c,0x1a,0x0a,0x08,0x1d,0x0c,0x36,0x00,0x1d,0x14}

io.write("Enter the flag: ")
local input = io.read()

if #input ~= #flag then
print("Incorrect!")
return
end

for i = 1, #input do
if string.char(bit32.bxor(flag[i], 0x69)) ~= input:sub(i, i) then
print("Incorrect!")
return
end
end

print("Correct!")